select Отчество, Повтор, Отдел
from (select Н_ЛЮДИ.ОТЧЕСТВО as Отчество,
count(Н_ЛЮДИ.ИД) as Повтор,
Н_ОТДЕЛЫ.КОРОТКОЕ_ИМЯ as Отдел
from (((Н_ЛЮДИ inner join Н_УЧЕНИКИ on Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД)
inner join Н_ПЛАНЫ on Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД)
inner join Н_ОТДЕЛЫ on Н_ПЛАНЫ.ОТД_ИД = Н_ОТДЕЛЫ.ИД)
group by 1,3)
where Отдел = 'КТиУ'
group by 1,2,3
having Повтор = 10;