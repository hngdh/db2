select Н_УЧЕНИКИ.ЧЛВК_ИД, Н_ОТДЕЛЫ.КОРОТКОЕ_ИМЯ != 'КТиУ'
from (Н_УЧЕНИКИ inner join Н_ПЛАНЫ on Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД)
inner join Н_ОТДЕЛЫ on Н_ПЛАНЫ.ОТД_ИД = Н_ОТДЕЛЫ.ИД
group by 1,2
having count(ЧЛВК_ИД) = 1 and КОРОТКОЕ_ИМЯ != 'КТиУ'
order by 1;